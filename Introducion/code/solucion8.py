cadena = input("Ingrese una cadena de texto: ")
inversa = cadena[::-1]
print("La cadena invertida es:", inversa)

"""
En este código, la variable cadena almacena la cadena 
de texto ingresada por el usuario mediante la función 
input(). Luego, se utiliza la técnica de rebanado con el 
operador [::-1] para invertir la cadena, y se almacena 
en la variable inversa. Finalmente, se imprime la cadena 
invertida mediante la función print().
"""