# Solicitamos al usuario que ingrese un número entero
numero_entero = int(input("Ingrese un número entero: "))

# Obtenemos el predecesor y sucesor del número entero
predecesor = numero_entero - 1
sucesor = numero_entero + 1

# Mostramos por pantalla el número entero ingresado junto con su predecesor y sucesor
print("El número ingresado es:", numero_entero)
print("Su predecesor es:", predecesor)
print("Su sucesor es:", sucesor)

