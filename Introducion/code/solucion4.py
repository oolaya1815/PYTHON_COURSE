# Solicitamos al usuario que ingrese una cadena de texto
cadena = input("Ingrese una cadena de texto: ")

# Obtenemos la cantidad de caracteres de la cadena y la almacenamos en una variable nueva
cantidad_caracteres = len(cadena)

# Mostramos la cantidad de caracteres por pantalla
print("La cantidad de caracteres de la cadena es:", cantidad_caracteres)

