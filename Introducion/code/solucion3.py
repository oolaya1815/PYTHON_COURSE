# Solicitamos al usuario que ingrese una cadena de texto
cadena = input("Ingrese una cadena de texto: ")

# Convertimos la cadena a mayúsculas y la almacenamos en una variable nueva
cadena_mayusculas = cadena.upper()

# Mostramos la cadena en mayúsculas por pantalla
print("La cadena en mayúsculas es:", cadena_mayusculas)

